// Get all ingredients as flat array
export const getAllIngredients = () => {
  return Object.values(INGREDIENTS_DATABASE).flat();
};

// Search ingredients
export const searchIngredients = (query) => {
  if (!query) return getAllIngredients();
  
  const allIngredients = getAllIngredients();
  return allIngredients.filter(ingredient => 
    ingredient.name.toLowerCase().includes(query.toLowerCase()) ||
    ingredient.id.toLowerCase().includes(query.toLowerCase())
  );
};

// Get ingredients by category
export const getIngredientsByCategory = (category) => {
  return INGREDIENTS_DATABASE[category] || [];
};

// Get ingredient by ID
export const getIngredientById = (id) => {
  const allIngredients = getAllIngredients();
  return allIngredients.find(ingredient => ingredient.id === id);
};
